package org.jtalks.jcommune.plugin.api.web.dto.json;

/**
 * Class contains information about one validation error
 * 
 * @author Vyacheslav Mishcheryakov
 */
public class ValidationError {
    
    /** Name of invalid field  */
    private String field;
    
    /** Resulted validation message */
    private String message;

    public ValidationError(String field, String defaultMessage) {
        super();
        this.field = field;
        this.message = defaultMessage;
    }

    /**
     * @return the field
     */
    public String getField() {
        return field;
    }

    /**
     * @param field the field to set
     */
    public void setField(String field) {
        this.field = field;
    }

    /**
     * @return the Message
     */
    public String getMessage() {
        return message;
    }

    /**
     * @param message the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }
    
    

}
